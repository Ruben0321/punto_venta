class Usuario:
    def __init__(self, p_contraseña=str(),p_correo=str(),p_id=int(),p_nombre=str(),p_puesto=str()):
       self.contraseña=p_contraseña
       self.correo=p_correo
       self.id= p_id
       self.nombre=p_nombre
       self.puesto=p_puesto
    def consultarUsu(self):
        return self.consultarUsu
    def eliminarUsu(self):
        return self.registrarUsu
    def modificarUsu(self):
        return self.eliminarUsu
    def registrarUsu(self):
        return self.registrarUsu