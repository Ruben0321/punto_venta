class Proveedores:
    def __init__(self, p_direccion=str(), p_id=int(), p_nombre=str(),p_telefono=str()):
        self.direccion=p_direccion
        self.id=p_id
        self.nombre=p_nombre
        self.telefono=p_telefono
    def consultar(self):
        return self.consultar
    def eliminar(self):
        return self.eliminar
    def modificar(self):
        return self.modificar
    def registrar(self):
        return self.registrar