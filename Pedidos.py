class Pedidos:
    def __init__(self,p_cantidad=int(), p_id=int(),p_producto=str(),p_proveedor=str()):
        self.cantidad=p_cantidad
        self.id=p_id
        self.producto=p_producto
        self.proveedor=p_proveedor
    def consultarPedi(self):
        return self.consultarPedi
    def eliminarPedi(self):
        return self.eliminarPedi
    def modificarPedi(self):
        return self.modificarPedi
    def realizarPedi(self):
        return self.realizarPedi